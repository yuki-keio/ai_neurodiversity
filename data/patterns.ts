
import { Pattern } from '../types';

// mainTextを動的に生成する関数
function generateMainText(pattern: Omit<Pattern, 'mainText'>): string {
  const parts: string[] = [];

  // カテゴリとグループ
  parts.push(`${pattern.category} > ${pattern.group}`);
  parts.push(`${pattern.numberText}`);
  parts.push(`${pattern.name}`);

  // 導入（パターン名の補足も兼ねて）
  if (pattern.id === 1) {
    parts.push(pattern.introduction);
  }

  parts.push(""); // 空行
  parts.push("");

  // 体験談
  if (pattern.exampleQuote) {
    parts.push(`${pattern.exampleQuote.person}：  ${pattern.exampleQuote.quote}`);
    parts.push("");
  }


  parts.push(pattern.context);
  parts.push("");

  // 問題
  parts.push("▼その状況において");
  parts.push("");
  parts.push(pattern.problem);
  parts.push(pattern.forces);
  parts.push("");

  // 解決策
  parts.push("▼そこで");
  parts.push("");
  parts.push(pattern.solution);
  parts.push(pattern.actions);
  parts.push("");


  // 結果
  parts.push("▼その結果");
  parts.push("");
  parts.push(pattern.consequences);
  parts.push("");


  // 関連パターン
  if (pattern.relatedPatterns && pattern.relatedPatterns.length > 0) {
    parts.push("");
    parts.push("関連パターン");
    pattern.relatedPatterns.forEach(related => {
      parts.push(`No.${related.number}　${related.text}`);
    });
  }

  return parts.join('\n');
}

